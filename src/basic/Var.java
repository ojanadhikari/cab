public class Var {
    public static void main(String[] args) {
        int a =1;
        System.out.println(a);
        byte b = 1;
        System.out.println(b);
        char c ='d';
        System.out.println(c);
        double d =3.14;
        System.out.println(d);
        float f =3.14f;
        System.out.println(f);
        long l =1234567890;
        System.out.println(l);
        boolean b1 =true;
        System.out.println(b1);
    }
}
